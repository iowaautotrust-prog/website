// Mutable vehicle list that admin can modify
let _vehicles: Vehicle[] = [...defaultVehicles];

export const vehicles = _vehicles;

export const getVehicles = (): Vehicle[] => _vehicles;

export const setVehicles = (v: Vehicle[]) => {
  _vehicles.length = 0;
  _vehicles.push(...v);
};

export const addVehicle = (v: Vehicle) => {
  _vehicles.push(v);
};

export const updateVehicle = (id: string, data: Partial<Vehicle>) => {
  const idx = _vehicles.findIndex((v) => v.id === id);
  if (idx !== -1) _vehicles[idx] = { ..._vehicles[idx], ...data };
};

export const deleteVehicle = (id: string) => {
  const idx = _vehicles.findIndex((v) => v.id === id);
  if (idx !== -1) _vehicles.splice(idx, 1);
};
